//DS1302
void Write_A_Byte_To_DS1302(uchar x)	  //向DS1302写入一个字节
{
	uchar i;
	for(i=0;i<8;i++)
	{
		SDA=x&1;
		SCLK=1;
		SCLK=0;
		x>>=1;
	}
}

uchar Get_A_Byte_From_DS1302()			  //从DS1302读取一个字节
{
	uchar i,b,t;
	for(i=0;i<8;i++)
	{
		b>>=1;
		t=SDA;
		b|=t<<7;
		SCLK=1;
		SCLK=0;
	}
	return b/16*10+b%16;
}

uchar Read_Data(uchar addr)				  //从DS1302指定位置读数据
{
	uchar dat;
	RST=0;
	SCLK=0;
	RST=1;
	Write_A_Byte_To_DS1302(addr);
	dat=Get_A_Byte_From_DS1302();
	SCLK=1;
	RST=0;
	return dat;
}

void Write_DS1302(uchar addr,uchar dat)	   //向DS1302某地址写入数据
{
	SCLK=0;
	RST=1;
	Write_A_Byte_To_DS1302(addr);
	Write_A_Byte_To_DS1302(dat);
	SCLK=0;
	RST=0;
}

