void WriteByte(uchar dat)	//写一个字节数据
{
	char i;
	for(i=0;i<8;i++)		//循环8次把编码传给锁存器
	{
		dat=dat>>1;			//右移一位，取出该字节的最低位
		R1=CY;				//将该字节的最低位传给R
		CLK=0;				//将数据移入595，上升沿
		CLK=1;
	}
}

